<script setup>
import { computed } from 'vue';

const emit = defineEmits(['update:checked']);

const props = defineProps({
    checked: {
        type: [Array, Boolean],
        required: true,
    },
    value: {
        default: null,
    },
});

const proxyChecked = computed({
    get() {
        return props.checked;
    },

    set(val) {
        emit('update:checked', val);
    },
});
</script>

<template>
    <input
        type="checkbox"
        :value="value"
        v-model="proxyChecked"
        class="rounded-md border-slate-300 text-primary-500 shadow-sm focus:ring-primary-500 dark:border-slate-600 dark:bg-slate-800 dark:focus:ring-primary-500 dark:focus:ring-offset-slate-800"
    />
</template>
